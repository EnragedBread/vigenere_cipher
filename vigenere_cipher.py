def encode(plaintext, secret_word):
    ciphertext = []
    pos = 0
    letter_count = len(secret_word)
    for letter in plaintext:
        column = alphabet.find(letter)
        if column < 0:
            ciphertext.append(letter)
        else:
            row = alphabet.find(secret_word[pos])
            pos = (pos + 1) % letter_count
            ciphertext.append(vigenere_matrix[row][column])
    return ''.join(ciphertext)

def decode(ciphertext, secret_word):
    plaintext = []
    pos = 0
    letter_count = len(secret_word)
    for letter in ciphertext:
        row = alphabet.find(secret_word[pos])
        try:
            column = vigenere_matrix[row].index(letter)
            pos = (pos + 1) % letter_count
            plaintext.append(vigenere_matrix[0][column])
        except ValueError:
            plaintext.append(letter)
    return ''.join(plaintext)